func main() {
	scanner := bufio.NewScanner(os.Stdin)
	for scanner.Scan() {
		root := utils.StringToTreeNode(scanner.Text())
		Printf("Output: %v\n", structures.TreeToLevelOrderStrings(pruneTree(root)))
	}
}
